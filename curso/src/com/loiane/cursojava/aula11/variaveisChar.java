package com.loiane.cursojava.aula11;

public class variaveisChar {

    public static void main(String[] args) {

        char o = 'o';
        char i = 'i';

        System.out.println(+o + i);

        char x = 105;
        char y = 111;
        char interrogacao = 0x003F;

        System.out.println(x + y);

        System.out.println("" + x + y + interrogacao);

    }
}
